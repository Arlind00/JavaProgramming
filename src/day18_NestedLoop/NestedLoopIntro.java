package day18_NestedLoop;

public class NestedLoopIntro {

    public static void main(String[] args) {


        for (int i = 0; i < 4 ; i++) {      // i: 1, 2, 3, 4

            for (int j = 0; j < 5; j++) {       // 1, 2, 3, 4, 5
                System.out.println( "Wooden Spoon");
            }
        }


    }
}
