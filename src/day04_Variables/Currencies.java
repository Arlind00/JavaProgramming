package day04_Variables;

public class Currencies {

    public static void main(String[] args) {

        int dolar = 1000;
        double jpn = dolar * 114.16;
        double pound = dolar * 0.73;
        double peso = dolar * 20.5;
        double cad = dolar * 1.25;
        double riyad = dolar * 3.75;
        double lira = dolar * 13.35;
        double euro = dolar * 0.88;


        System.out.println("this amount of dolar = " + dolar);
        System.out.println("equals to...");
        System.out.println("jpn = " + jpn);
        System.out.println("pound = " + pound);
        System.out.println("peso = " + peso);
        System.out.println("riyad = " + riyad);
        System.out.println("cad = " + cad);
        System.out.println("euro = " + euro);
        System.out.println("lira = " + lira);
    }
}
