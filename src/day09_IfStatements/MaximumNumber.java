package day09_IfStatements;

public class MaximumNumber {

    public static void main(String[] args) {

        int num1 = 50;
        int num2 = 49;

        if ( num1 > num2){
            System.out.println("Maximum number is : " + num1);
        }else{
            System.out.println("Maximum number is : " + num2);
        }
    }
}
