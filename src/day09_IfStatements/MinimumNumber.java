package day09_IfStatements;

public class MinimumNumber {

    public static void main(String[] args) {

        int num1 = 50;
        int num2 = 49;

        if (num1 > num2) {
            System.out.println("Minimum number is : " + num2);
        } else {
            System.out.println("Minimum number is : " + num1);
        }

    }
}
