package day31_Constructors.TASKS;

public class Casts {

    public String name;
    public String surname;

    public Casts(String name, String surname) {
        this.name = name;
        this.surname = surname;
    }

    public String toString() {
        return "Casts{" +
                "name='" + name + '\'' +
                ", surname='" + surname + '\'' +
                '}';
    }
}
