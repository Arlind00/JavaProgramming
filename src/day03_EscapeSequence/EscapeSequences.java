package day03_EscapeSequence;

/*
Escape sequences: MUST be given without a double quote
    \n: starts new line
    \t: tab (paragraph space)
    \\: single back slash (line 18 of  this code)
    \": double code in java
 */
public class EscapeSequences {

    public static void main(String[] args) {

        System.out.println("Java \nPython \nC#");
        System.out.println("--------------------");
        System.out.println("Hello everyone \n How are you today? \nToday we will learn Escape Sequence, \nand next week we will learn variables ");
        System.out.println("--------------------");
        System.out.println("\tJava is cool programming languale");
        System.out.println("/\\");
        System.out.println("My favorite movie \"Robin Hood\"");// in order to print


    }
}
